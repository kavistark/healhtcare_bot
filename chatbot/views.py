def apply_sidebar_filters(df, filters):
    """Filters the HCP dataframe based on active sidebar filter options from client."""
    if not filters:
        return df

    filtered_df = df.copy()

    # Filter by channel
    channels = filters.get('channel', [])
    if channels:
        filtered_df = filtered_df[filtered_df['channel'].isin(channels)]

    # Filter by writer_status
    writer_statuses = filters.get('writer_status', [])
    if writer_statuses:
        filtered_df = filtered_df[filtered_df['writer_status'].isin(writer_statuses)]

    # Filter by patient_type
    patient_types = filters.get('patient_type', [])
    if patient_types:
        filtered_df = filtered_df[filtered_df['patient_type'].isin(patient_types)]

    # Filter by territory
    territories = filters.get('territory', [])
    if territories:
        filtered_df = filtered_df[filtered_df['territory'].isin(territories)]

    # Filter by threshold days for action-needed (follow-up = None) alerts
    threshold = filters.get('threshold', 14)
    if threshold is not None and 'received_date' in filtered_df.columns:
        from datetime import datetime
        as_of_date = datetime(2026, 3, 6)
        
        def check_threshold(row):
            if row.get('follow_up_status') != 'None':
                return True
            try:
                rec_date = datetime.strptime(str(row.get('received_date')), "%m-%d-%Y")
                waiting_days = (as_of_date - rec_date).days
                return waiting_days >= threshold
            except Exception:
                return True
                
        filtered_df = filtered_df[filtered_df.apply(check_threshold, axis=1)]

    return filtered_df

