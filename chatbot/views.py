# Global cache for data and predictor model
_df_cached = None
_predictor_cached = None

def get_hcp_data_and_predictor():
    """Lazy loads, trains, and caches the HCP dataset and ML predictor using the JAKAFI schema."""
    global _df_cached, _predictor_cached
    if _df_cached is not None and _predictor_cached is not None:
        return _df_cached, _predictor_cached
        
    predictor = HCPPredictor()
    if os.path.exists(CSV_PATH):
        # Train model on current CSV
        predictor.train(CSV_PATH)
        df = pd.read_csv(CSV_PATH)
        
        # Enforce data types
        df['prior_week_rx'] = pd.to_numeric(df['prior_week_rx'], errors='coerce').fillna(0).astype(int)
        df['current_week_rx'] = pd.to_numeric(df['current_week_rx'], errors='coerce').fillna(0).astype(int)
        df['follow_up_status'] = df['follow_up_status'].fillna('None')
        
        # Generate ML predictions
        df['Predicted_Next_Week_Rx'] = predictor.predict_trends(df)
        
        # Calculate priorities
        scores = []
        reasons_list = []
        for _, row in df.iterrows():
            score, reasons = predictor.calculate_priority_score(row)
            scores.append(score)
            reasons_list.append("; ".join(reasons))
            
        df['Priority_Score'] = scores
        df['Priority_Reasons'] = reasons_list
        
        _df_cached = df
        _predictor_cached = predictor
    else:
        _df_cached = pd.DataFrame()
        _predictor_cached = predictor
        
    return _df_cached, _predictor_cached

