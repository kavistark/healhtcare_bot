def retrieve_relevant_rows(query, df, top_k=10):
    """Retrieves the top_k most relevant rows from the dataframe using TF-IDF similarity to the query."""
    if df.empty:
        return []
    
    # Create textual representation for each row
    row_texts = []
    for _, row in df.iterrows():
        text_parts = []
        for col in df.columns:
            text_parts.append(f"{col}: {row[col]}")
        row_texts.append(" | ".join(text_parts))
        
    from sklearn.feature_extraction.text import TfidfVectorizer
    from sklearn.metrics.pairwise import cosine_similarity
    
    vectorizer = TfidfVectorizer(stop_words='english')
    # Fit and transform
    tfidf_matrix = vectorizer.fit_transform(row_texts + [query])
    similarities = cosine_similarity(tfidf_matrix[-1], tfidf_matrix[:-1]).flatten()
    
    # Sort indices descending
    top_indices = similarities.argsort()[::-1][:top_k]
    
    results = []
    for idx in top_indices:
        results.append(df.iloc[idx])
    return results

def parse_local_query_filters(query, df):
    """
    Parses natural language queries to dynamically filter and rank JAKAFI database columns.
    Returns: filtered_df, filters_applied, sort_by, ascending, limit
    """
    filtered_df = df.copy()
    filters_applied = []
    sort_by = None
    ascending = False
    limit = None
    
    msg = query.lower()
    
    # 1. Territory filtering
    unique_territories = df['territory'].dropna().unique()
    for t in unique_territories:
        if str(t).lower() in msg:
            filtered_df = filtered_df[filtered_df['territory'] == t]
            filters_applied.append(f"Territory: **{t}**")
            break
            
    # 2. Specialty filtering
    if 'specialty' in df.columns:
        unique_specs = df['specialty'].dropna().unique()
        for s in unique_specs:
            if str(s).lower() in msg:
                filtered_df = filtered_df[filtered_df['specialty'] == s]
                filters_applied.append(f"Specialty: **{s}**")
                break
                
    # 3. Channel filtering
    for c in ['direct', 'spp']:
        if c in msg:
            c_val = 'Direct' if c == 'direct' else 'SPP'
            filtered_df = filtered_df[filtered_df['channel'] == c_val]
            filters_applied.append(f"Channel: **{c_val}**")
            break
            
    # 4. Patient Type filtering
    if 'new patient' in msg:
        filtered_df = filtered_df[filtered_df['patient_type'] == 'New Patient']
        filters_applied.append("Patient Type: **New Patient**")
    elif 'established patient' in msg or ('established' in msg and 'writer' not in msg):
        filtered_df = filtered_df[filtered_df['patient_type'] == 'Established Patient']
        filters_applied.append("Patient Type: **Established Patient**")
        
    # 5. Writer Status filtering
    if 'new writer' in msg or 'new writers' in msg:
        filtered_df = filtered_df[filtered_df['writer_status'] == 'New Writer']
        filters_applied.append("Writer Status: **New Writer**")
    elif 'established writer' in msg or 'established writers' in msg:
        filtered_df = filtered_df[filtered_df['writer_status'] == 'Established Writer']
        filters_applied.append("Writer Status: **Established Writer**")
    elif 'lapsed writer' in msg or 'lapsed writers' in msg or 'lapsed' in msg:
        filtered_df = filtered_df[filtered_df['writer_status'] == 'Lapsed Writer']
        filters_applied.append("Writer Status: **Lapsed Writer**")
        
    # 6. Follow-up Status filtering
    if any(k in msg for k in ['action', 'pending', 'required', 'overdue', 'follow-up none']) or ('active' in msg and 'alert' in msg):
        filtered_df = filtered_df[filtered_df['follow_up_status'] == 'None']
        filters_applied.append("Follow-up: **None**")
    elif 'scheduled' in msg:
        filtered_df = filtered_df[filtered_df['follow_up_status'] == 'Scheduled']
        filters_applied.append("Follow-up: **Scheduled**")
    elif 'completed' in msg:
        filtered_df = filtered_df[filtered_df['follow_up_status'] == 'Completed']
        filters_applied.append("Follow-up: **Completed**")
        
    # 7. Sorting configuration
    if any(k in msg for k in ['priority', 'highest', 'top', 'score', 'urgent', 'urgency']):
        sort_by = 'Priority_Score'
        ascending = False
    elif any(k in msg for k in ['growth', 'forecast', 'predict', 'ml', 'trend', 'next week']):
        if 'Predicted_Next_Week_Rx' in df.columns:
            filtered_df['expected_growth'] = filtered_df['Predicted_Next_Week_Rx'] - filtered_df['current_week_rx']
            sort_by = 'expected_growth'
            ascending = False
            
    # 8. Row limit checking
    import re
    limit_match = re.search(r'\b(top|first|show|limit)\s+(\d+)\b', msg)
    if limit_match:
        limit = int(limit_match.group(2))
    elif 'top' in msg or 'highest' in msg:
        limit = 5
        
    return filtered_df, filters_applied, sort_by, ascending, limit

def generate_dynamic_filtered_report(query, df):
    """Dynamically parses queries to analyze datasets when RAG/LLM rate limit fallbacks trigger."""
    filtered_df, filters, sort_by, ascending, limit = parse_local_query_filters(query, df)
    
    if sort_by:
        filtered_df = filtered_df.sort_values(by=sort_by, ascending=ascending)
        
    total_found = len(filtered_df)
    if limit:
        filtered_df = filtered_df.head(limit)
        
    if filtered_df.empty:
        filter_str = " and ".join(filters) if filters else "those keywords"
        return f"I analyzed the JAKAFI alerts database, but found no records matching: **{filter_str}**.", ["Show active JAKAFI alerts", "Summarize prescription KPIs"]
        
    # Build dynamically formatted Markdown Table
    columns_to_show = ['hcp_id', 'hcp_name', 'specialty', 'territory', 'channel', 'patient_type', 'writer_status', 'current_week_rx', 'follow_up_status', 'Priority_Score']
    cols = [c for c in columns_to_show if c in filtered_df.columns]
    
    table_markdown = "| " + " | ".join([c.replace('_', ' ').title() for c in cols]) + " |\n"
    table_markdown += "| " + " | ".join([":---" for _ in cols]) + " |\n"
    
    for _, row in filtered_df.iterrows():
        row_vals = []
        for c in cols:
            val = row[c]
            if c == 'Priority_Score':
                row_vals.append(f"**{val}**")
            else:
                row_vals.append(str(val))
        table_markdown += "| " + " | ".join(row_vals) + " |\n"
        
    filter_desc = f" filtered by {', '.join(filters)}" if filters else ""
    sort_desc = f", sorted by **{sort_by.replace('_', ' ').title()}**" if sort_by else ""
    limit_desc = f" (showing top {limit} of {total_found} matches)" if limit and total_found > limit else f" (showing all {total_found} matching records)"
    
    text = (
        f"### 🔍 Dynamic Database Analysis\n"
        f"I analyzed the database and found JAKAFI accounts{filter_desc}{sort_desc}{limit_desc}:\n\n"
        f"{table_markdown}\n"
        f"Would you like to search for a specific doctor or summarize prescription KPIs?"
    )
    suggestions = ["Show active JAKAFI alerts", "Summarize prescription KPIs", "Forecast next week's Rx growth"]
    return text, suggestions

